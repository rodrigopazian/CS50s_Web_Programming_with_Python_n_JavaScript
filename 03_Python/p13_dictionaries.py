#Creating dictionary
houses = {"Harry": "Gryffindor", "Draco": "Slytherin"}

print(houses["Harry"])
print(houses["Draco"])
print(houses)


#Adding Hermione
houses["Hermione"] = "Gryffindor"

print(houses["Hermione"])
print(houses)


#Changing Draco
houses["Draco"] = "Sonserina"

print(houses["Draco"])
print(houses)